package io.github.tanu31195.audionirvana;

public class BluetoothSpeaker {
    private String model;
    private String size;
    private String batteryLife;
    private boolean multipleConnectivity;
    private String range;
    private String durability;
    private boolean waterproof;
    private String cost;
    private String url;

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getBatteryLife() {
        return batteryLife;
    }

    public void setBatteryLife(String batteryLife) {
        this.batteryLife = batteryLife;
    }

    public boolean isMultipleConnectivity() {
        return multipleConnectivity;
    }

    public void setMultipleConnectivity(boolean multipleConnectivity) {
        this.multipleConnectivity = multipleConnectivity;
    }

    public String getRange() {
        return range;
    }

    public void setRange(String range) {
        this.range = range;
    }

    public String getDurability() {
        return durability;
    }

    public void setDurability(String durability) {
        this.durability = durability;
    }

    public boolean isWaterproof() {
        return waterproof;
    }

    public void setWaterproof(boolean waterproof) {
        this.waterproof = waterproof;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
